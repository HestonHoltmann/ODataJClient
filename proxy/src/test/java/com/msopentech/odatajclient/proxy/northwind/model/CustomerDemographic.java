package com.msopentech.odatajclient.proxy.northwind.model;

import com.msopentech.odatajclient.proxy.api.annotations.EntityType;
import com.msopentech.odatajclient.proxy.api.annotations.Key;
import com.msopentech.odatajclient.proxy.api.annotations.NavigationProperty;
import com.msopentech.odatajclient.proxy.api.annotations.Property;

import java.io.Serializable;
import java.util.List;

@EntityType("CustomerDemographic")
public class CustomerDemographic implements Serializable {

    private static final long serialVersionUID = -8841329287647045253L;

    @Key
    @Property(name = "CustomerTypeID", type = "Edm.String", nullable = false)
    private String customerTypeID;

    @Property(name = "CustomerDesc", type = "Edm.String", nullable = true)
    private String customerDesc;

    @NavigationProperty(name = "Customers", relationship = "NorthwindModel.CustomerCustomerDemo",
            fromRole = "CustomerDemographics", toRole = "Customers")
    private List<Customer> customers;

    public String getCustomerTypeID() {
        return customerTypeID;
    }

    public void setCustomerTypeID(String customerTypeID) {
        this.customerTypeID = customerTypeID;
    }

    public String getCustomerDesc() {
        return customerDesc;
    }

    public void setCustomerDesc(String customerDesc) {
        this.customerDesc = customerDesc;
    }

    public List<Customer> getCustomers() {
        return customers;
    }

    public void setCustomers(List<Customer> customers) {
        this.customers = customers;
    }
}
